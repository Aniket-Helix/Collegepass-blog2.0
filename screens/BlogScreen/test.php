<!-- wp:paragraph -->
 <p align="justify">The University of California is ranked among the world’s best for its innovation, pioneering research, and discovery. It has ten campuses, with nine offering both undergraduate and graduate education. These nine main campuses are</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:list {"style":{"color":{"text":"#e7e7e7"}}} -->
 <ul class="has-text-color" style="color:#e7e7e7"><li>UC Berkeley,</li><li>UC Los Angeles,</li><li>UC Davis,</li><li>UC San Diego,</li><li>UC Santa Barbara,</li><li>UC Irvine,</li><li>UC Merced,</li><li>UC Santa Cruz,</li><li>UC Riverside.</li></ul>
 <!-- /wp:list -->
 
 <!-- wp:paragraph -->
 <p align="justify">UC San Francisco is a professional and a graduate school only.</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:paragraph -->
 <p align="justify">The University of California opened its doors in 1869 with just 10 faculty members and 40 students. Today, the UC system has more than 280,000 students and 227,000 faculty and staff, with 2.0 million alumni living and working around the world. *</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:paragraph -->
 <p align="justify">According to the data, the university of California admitted fewer out-of-state and international students for fall 2022. These trends varied across the nine campuses. The Berkeley and Los Angeles campuses accepted more Californians compared to 2021. Out-of-the-state applicants admitted across the system were 22,798, a 19% drop from last year. The international freshman applicants admitted were 17,531, which is 12% less than in 2021.</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:heading {"level":4} -->
 <h4>UNIVERSITY OF CALIFORNIA: APPLICATION PROCESS</h4>
 <!-- /wp:heading -->
 
 <!-- wp:paragraph -->
 <p align="justify"><strong>EDUCATION SYSTEM REQUIREMENTS FOR INDIAN STUDENTS</strong> – Applicants must finish the 10th and 12th CBSE examination or state board, with average marks being above 70 and not below 60. Under the test scores tab, report your marks and subjects on the International Exam page of the application.</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:paragraph -->
 <p align="justify"><strong>ACT/SAT REQUIREMENTS</strong> – The University of California does not consider ACT or SAT scores when making admissions decisions. If you want to submit test scores, they can be used as an alternative procedure for attaining minimum requirements for course placement or eligibility after you enroll.</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:paragraph -->
 <p align="justify"><strong>ENGLISH PROFICIENCY REQUIREMENTS</strong> – Your TOEFL score on the internet-based test must be at least 87; for the paper and pencil test, at least 560 is required. IELTS overall band score should at least be 7.0. The DET test scores should be 120 or higher.</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:paragraph -->
 <p align="justify"><strong>LETTER OF RECOMMENDATION</strong> – The University of California does not require letters of recommendation for the application. However, some majors and campuses may require LORs as a supplemental application review.</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:paragraph -->
 <p align="justify"><strong>PERSONAL INSIGHTS </strong>– University of California application requires an applicant to answer 4 personal insight questions, with each prompt being within the 350-word limit range. An applicant must decide which of the four prompts they plan on answering out of the eight. The prompts you choose must highlight your skills, creativity, and experiences.</p>
 <!-- /wp:paragraph -->
 
 <!-- wp:paragraph -->
 <p align="justify"><strong>FINANCIAL AID</strong> – International students are not eligible for student loans, and very few scholarships are available. Costs for attending school during the summer and living expenses are not a part of the cost of attendance.</p>
